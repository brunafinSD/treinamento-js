console.log('"9" + 5')

console.log('coersão automática do javascript transformando 5 em string')
console.log('9' + 5); // concatenação, o JavaScript fez uma coersão transformando o number 5 para string e concatenou os valores

console.log('conversão gerada por mim fazendo o casting do "9" para number')
console.log(Number('9') + 5); // usei o typecasting para converter o '9' em number